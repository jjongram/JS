const calculator = {
  add: function (a, b) {
    return a + b;
  },
  min: function (a, b) {
    return a - b;
  },
  div: function (a, b) {
    return a / b;
  },
  powerof: function (a, b) {
    return a ** b;
  },
};

calculator.add(5, 2);
calculator.min(5, 2);
calculator.div(5, 2);
calculator.powerof(5, 2);
